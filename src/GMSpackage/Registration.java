    // Метод регистрации новых пользователей
    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        // Проверка на непустоту полей
        if (!((jTextField1.getText().equals("") || jTextField2.getText().equals("")))) {
            if (jComboBox1.getSelectedItem().toString() == "Администратор") {
                if (Form.gms.register_admin(jTextField1.getText(), jTextField2.getText())) {
                JOptionPane.showMessageDialog(reg, "Администратор успешно зарегистрирован");
                }
                else JOptionPane.showMessageDialog(reg, "Такой пользователь уже существует", "Registration error", JOptionPane.ERROR_MESSAGE);
            }
            else if (jComboBox1.getSelectedItem().toString() == "Член команды") {
                if (Form.gms.register_member(jTextField1.getText(), jTextField2.getText())) {               
                JOptionPane.showMessageDialog(reg, "Член команды успешно зарегистрирован");
                } 
                else JOptionPane.showMessageDialog(reg, "Такой пользователь уже существует", "Registration error", JOptionPane.ERROR_MESSAGE);
            }
        }
        else JOptionPane.showMessageDialog(reg, "Некорректные параметры ввода", "Registration error", JOptionPane.ERROR_MESSAGE);
    }//GEN-LAST:event_jButton1ActionPerformed

